input_text = input()

if input_text == "banana" or input_text == "apple" or input_text == "kiwi" or input_text == "cherry" or input_text ==\
        "lemon" or input_text == "grapes":
    print("fruit")
elif input_text == "tomato" or input_text == "cucumber" or input_text == "pepper" or input_text == "carrot":
    print("vegetable")
else:
    print("unknown")